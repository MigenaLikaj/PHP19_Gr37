 atdhetare në mënyrë të veçantë në shpërndarjen e librave shqip në krejt jugun e Shqipërisë.
 Falë përkushtimit të tij të plotë, Abaz Skëndi merr dorë tek babai i teqesë dhe bëhet myhib
 (shkalla e parë e praktikantëve mistik të bektashizmit). Deri sa ndërron jetë në vitin 1911,
 ky shqiptar i madh i shërben me devotshmëri kombit të vet.
Ali Abaz Skëndi kryen arsimin fillor në Berat, ndërsa të mesmen ne Kozan ku mëson,
 veç greqishtes, edhe gjuhët arabe e turke. Në vitin 1913 kthehet në atdhe dhe
 vazhdon punën e të atit në teqenë e Prishtës.
