<p align="middle">Ali Abaz Skëndi, i quajtur ndryshe baba Ali Tomori,
 u lind më 1893 në fshatin Shalës në Tepelenë. Ishte i biri i Abazit,
 i cili kishte mbaruar shkollën e lartë për nëpunës në Stamboll dhe emëruar në Kozan të Greqisë,
 ku shërbeu për 23 vjet. Në vitin 1902 Abazi kthehet në Shqipëri dhe i prirur të ndikojë sadopak në lëvizjen autonomiste shqiptare,
 i bashkëngjitet veprimit energjik të klerikëve bektashianë.
